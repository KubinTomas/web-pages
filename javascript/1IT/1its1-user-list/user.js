class User {
    id;
    firstName;
    surname;
    email;
    phone;
    age;
    company;

    constructor(id, firstName, surname, email, phone, age, company) {
        this.id = id;
        this.firstName = firstName;
        this.surname = surname;
        this.email = email;
        this.phone = phone;
        this.age = age;
        this.company = company;
    }
}