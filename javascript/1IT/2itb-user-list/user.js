class User {
    id;
    firstName;
    lastName;
    email;
    phone;
    age;
    company;

    constructor(id, firstName, lastName, email, phone, age, company) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phone = phone;
        this.age = age;
        this.company = company;
    }
}